package utils;

import com.radkevich.entity.CustomArray;

public class ArrayAlgorithmUtil {

    public int[] sortBubble(CustomArray unsortedArray) {

        int[] array = unsortedArray.getElements();
        int n = array.length;
        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (array[j] > array[j + 1]) {
                    int temp = array[j];
                    array[j] = array[j + 1];
                    array[j + 1] = temp;
                }
            }
        }
        return array;

    }

    public int[] sortSelection(CustomArray unsortedArray) {

        int[] array = unsortedArray.getElements();
        int n = array.length;
        for (int i = 0; i < n - 1; i++) {
            int minIdx = i;
            for (int j = i + 1; j < n; j++) {
                if (array[j] < array[minIdx]) {
                    minIdx = j;
                }
            }
            int temp = array[minIdx];
            array[minIdx] = array[i];
            array[i] = temp;
        }
        return array;

    }
}
